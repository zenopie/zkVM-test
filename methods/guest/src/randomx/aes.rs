/// GF(2^8) multiplication by 2
fn xtime(x: u8) -> u8 {
    if x & 0x80 != 0 {
        (x << 1) ^ 0x1b
    } else {
        x << 1
    }
}

/// GF(2^8) multiplication
fn gmul(mut a: u8, mut b: u8) -> u8 {
    let mut p: u8 = 0;
    for _ in 0..8 {
        if b & 1 != 0 {
            p ^= a;
        }
        let hi_bit = a & 0x80;
        a <<= 1;
        if hi_bit != 0 {
            a ^= 0x1b;
        }
        b >>= 1;
    }
    p
}

/// AES state (4x4 bytes = 16 bytes)
#[derive(Clone, Copy)]
pub struct AesState {
    pub state: [u8; 16],
}

impl AesState {
    pub fn new() -> Self {
        Self { state: [0u8; 16] }
    }

    pub fn from_bytes(bytes: &[u8]) -> Self {
        let mut state = [0u8; 16];
        state.copy_from_slice(&bytes[..16]);
        Self { state }
    }

    pub fn to_bytes(&self) -> [u8; 16] {
        self.state
    }

    /// SubBytes transformation
    pub fn sub_bytes(&mut self) {
        for byte in self.state.iter_mut() {
            *byte = SBOX[*byte as usize];
        }
    }

    /// Inverse SubBytes
    pub fn inv_sub_bytes(&mut self) {
        for byte in self.state.iter_mut() {
            *byte = INV_SBOX[*byte as usize];
        }
    }

    /// ShiftRows transformation
    pub fn shift_rows(&mut self) {
        // Row 1: shift left by 1
        let tmp = self.state[1];
        self.state[1] = self.state[5];
        self.state[5] = self.state[9];
        self.state[9] = self.state[13];
        self.state[13] = tmp;

        // Row 2: shift left by 2
        let tmp0 = self.state[2];
        let tmp1 = self.state[6];
        self.state[2] = self.state[10];
        self.state[6] = self.state[14];
        self.state[10] = tmp0;
        self.state[14] = tmp1;

        // Row 3: shift left by 3 (= shift right by 1)
        let tmp = self.state[15];
        self.state[15] = self.state[11];
        self.state[11] = self.state[7];
        self.state[7] = self.state[3];
        self.state[3] = tmp;
    }

    /// Inverse ShiftRows
    pub fn inv_shift_rows(&mut self) {
        // Row 1: shift right by 1
        let tmp = self.state[13];
        self.state[13] = self.state[9];
        self.state[9] = self.state[5];
        self.state[5] = self.state[1];
        self.state[1] = tmp;

        // Row 2: shift right by 2
        let tmp0 = self.state[10];
        let tmp1 = self.state[14];
        self.state[10] = self.state[2];
        self.state[14] = self.state[6];
        self.state[2] = tmp0;
        self.state[6] = tmp1;

        // Row 3: shift right by 3 (= shift left by 1)
        let tmp = self.state[3];
        self.state[3] = self.state[7];
        self.state[7] = self.state[11];
        self.state[11] = self.state[15];
        self.state[15] = tmp;
    }

    /// MixColumns transformation
    pub fn mix_columns(&mut self) {
        for col in 0..4 {
            let i = col * 4;
            let a = self.state[i];
            let b = self.state[i + 1];
            let c = self.state[i + 2];
            let d = self.state[i + 3];

            self.state[i] = xtime(a) ^ xtime(b) ^ b ^ c ^ d;
            self.state[i + 1] = a ^ xtime(b) ^ xtime(c) ^ c ^ d;
            self.state[i + 2] = a ^ b ^ xtime(c) ^ xtime(d) ^ d;
            self.state[i + 3] = xtime(a) ^ a ^ b ^ c ^ xtime(d);
        }
    }

    /// Inverse MixColumns
    pub fn inv_mix_columns(&mut self) {
        for col in 0..4 {
            let i = col * 4;
            let a = self.state[i];
            let b = self.state[i + 1];
            let c = self.state[i + 2];
            let d = self.state[i + 3];

            self.state[i] = gmul(a, 0x0e) ^ gmul(b, 0x0b) ^ gmul(c, 0x0d) ^ gmul(d, 0x09);
            self.state[i + 1] = gmul(a, 0x09) ^ gmul(b, 0x0e) ^ gmul(c, 0x0b) ^ gmul(d, 0x0d);
            self.state[i + 2] = gmul(a, 0x0d) ^ gmul(b, 0x09) ^ gmul(c, 0x0e) ^ gmul(d, 0x0b);
            self.state[i + 3] = gmul(a, 0x0b) ^ gmul(b, 0x0d) ^ gmul(c, 0x09) ^ gmul(d, 0x0e);
        }
    }

    /// XOR with round key
    pub fn add_round_key(&mut self, key: &[u8; 16]) {
        for (s, k) in self.state.iter_mut().zip(key.iter()) {
            *s ^= *k;
        }
    }
}

/// Perform one AES encryption round (without key addition)
/// This is what RandomX calls "soft AES"
pub fn aes_round(state: &mut AesState, key: &[u8; 16]) {
    state.sub_bytes();
    state.shift_rows();
    state.mix_columns();
    state.add_round_key(key);
}

/// Perform one AES decryption round (without key addition)
pub fn aes_round_dec(state: &mut AesState, key: &[u8; 16]) {
    state.inv_shift_rows();
    state.inv_sub_bytes();
    state.add_round_key(key);
    state.inv_mix_columns();
}

/// RandomX uses a special "soft AES" that's just the round function
/// applied multiple times with fixed keys
pub struct SoftAes;

impl SoftAes {
    /// Fill scratchpad using AES
    /// Takes 64-byte state, applies AES rounds, produces filled scratchpad
    pub fn fill_scratchpad(seed: &[u8; 64], scratchpad: &mut [u8]) {
        // RandomX scratchpad fill uses 4 parallel AES states
        let mut states = [
            AesState::from_bytes(&seed[0..16]),
            AesState::from_bytes(&seed[16..32]),
            AesState::from_bytes(&seed[32..48]),
            AesState::from_bytes(&seed[48..64]),
        ];

        // Fixed keys for scratchpad fill (from RandomX spec)
        let keys: [[u8; 16]; 4] = [
            [0xd7, 0x98, 0x3a, 0xad, 0x14, 0xab, 0x20, 0xdc, 0xa2, 0x9e, 0x6e, 0x02, 0x5f, 0x45, 0xb1, 0x1b],
            [0xbb, 0x04, 0x5d, 0x78, 0x45, 0x79, 0x98, 0x50, 0xd7, 0xdf, 0x28, 0xe5, 0x32, 0xe0, 0x48, 0xa7],
            [0xf1, 0x07, 0x59, 0xea, 0xc9, 0x72, 0x38, 0x2d, 0x67, 0x15, 0x88, 0x6c, 0x32, 0x59, 0x28, 0xab],
            [0x76, 0x9a, 0x49, 0xf0, 0x60, 0x14, 0xb6, 0x2c, 0xa9, 0x41, 0xc8, 0x19, 0x54, 0xb6, 0x75, 0xf7],
        ];

        let mut offset = 0;
        while offset < scratchpad.len() {
            // Apply 4 AES rounds to each state
            for state in states.iter_mut() {
                for key in keys.iter() {
                    aes_round(state, key);
                }
            }

            // Write states to scratchpad
            for state in states.iter() {
                let bytes = state.to_bytes();
                let end = core::cmp::min(offset + 16, scratchpad.len());
                let len = end - offset;
                scratchpad[offset..end].copy_from_slice(&bytes[..len]);
                offset += 16;
                if offset >= scratchpad.len() {
                    break;
                }
            }
        }
    }

    /// AES hash for final hash computation
    /// Takes 64-byte input, produces 64-byte output
    pub fn hash(input: &[u8; 64]) -> [u8; 64] {
        let mut states = [
            AesState::from_bytes(&input[0..16]),
            AesState::from_bytes(&input[16..32]),
            AesState::from_bytes(&input[32..48]),
            AesState::from_bytes(&input[48..64]),
        ];

        // Fixed keys for AES hash
        let keys: [[u8; 16]; 4] = [
            [0x35, 0x53, 0x45, 0x41, 0x88, 0x75, 0x4f, 0x8b, 0xca, 0xf5, 0xd4, 0x09, 0x8d, 0x93, 0x74, 0xd0],
            [0x07, 0xa4, 0x79, 0x25, 0x40, 0xc5, 0x23, 0x75, 0xe2, 0x18, 0xa1, 0xaa, 0x0f, 0xa0, 0xf0, 0xa5],
            [0xe8, 0xda, 0xf9, 0xac, 0x71, 0x9e, 0x77, 0xa0, 0x00, 0xc1, 0xd2, 0xae, 0x3e, 0xf4, 0x66, 0x81],
            [0x4c, 0x77, 0xd8, 0x36, 0xbb, 0xa3, 0xf6, 0x05, 0x88, 0x4a, 0x01, 0xdc, 0x8a, 0xa7, 0x14, 0xd9],
        ];

        // Apply AES rounds
        for _ in 0..4 {
            for (i, state) in states.iter_mut().enumerate() {
                aes_round(state, &keys[i]);
            }
        }

        // XOR states together for final output
        let mut output = [0u8; 64];
        for (i, state) in states.iter().enumerate() {
            output[i * 16..(i + 1) * 16].copy_from_slice(&state.to_bytes());
        }
        output
    }
}

/// AES generator for program randomization
pub struct AesGenerator {
    state: [AesState; 4],
    keys: [[u8; 16]; 4],
}

impl AesGenerator {
    pub fn new(seed: &[u8; 64]) -> Self {
        let state = [
            AesState::from_bytes(&seed[0..16]),
            AesState::from_bytes(&seed[16..32]),
            AesState::from_bytes(&seed[32..48]),
            AesState::from_bytes(&seed[48..64]),
        ];

        let keys: [[u8; 16]; 4] = [
            [0x06, 0x3c, 0xf1, 0x71, 0x7d, 0x2f, 0xa4, 0x08, 0x3b, 0x78, 0x88, 0xfe, 0x98, 0x5c, 0xf0, 0x67],
            [0x35, 0x5d, 0x29, 0x1d, 0x9a, 0xe5, 0xa3, 0x28, 0x46, 0xbf, 0x8e, 0x48, 0x29, 0xc1, 0xbc, 0x2a],
            [0x2a, 0xe9, 0x90, 0x27, 0x4f, 0x65, 0xd7, 0x43, 0xdb, 0xdd, 0x65, 0x03, 0xad, 0xf9, 0x95, 0x69],
            [0x8c, 0x18, 0x6b, 0x43, 0xa0, 0x49, 0xe9, 0x22, 0x0d, 0x8d, 0x7e, 0x52, 0x95, 0x08, 0xbf, 0xe6],
        ];

        Self { state, keys }
    }

    /// Generate next 64 bytes
    pub fn next_block(&mut self) -> [u8; 64] {
        for (state, key) in self.state.iter_mut().zip(self.keys.iter()) {
            aes_round(state, key);
        }

        let mut output = [0u8; 64];
        for (i, state) in self.state.iter().enumerate() {
            output[i * 16..(i + 1) * 16].copy_from_slice(&state.to_bytes());
        }
        output
    }

    /// Fill buffer with random bytes
    pub fn fill(&mut self, buffer: &mut [u8]) {
        let mut offset = 0;
        while offset < buffer.len() {
            let block = self.next_block();
            let remaining = buffer.len() - offset;
            let to_copy = core::cmp::min(remaining, 64);
            buffer[offset..offset + to_copy].copy_from_slice(&block[..to_copy]);
            offset += 64;
        }
    }
}
